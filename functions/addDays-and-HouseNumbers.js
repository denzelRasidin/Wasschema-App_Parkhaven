exports.addDays = functions.https.onRequest((request, response) => {
  return cors(request, response, () => {
    const batch = admin.firestore().batch();
    const days = [
      {id: '2018-09-26', isDisplayable: true, isCurrentWeek: true},
      {id: '2018-09-27', isDisplayable: true, isCurrentWeek: true},
      {id: '2018-09-28', isDisplayable: true, isCurrentWeek: true},
      {id: '2018-09-29', isDisplayable: true, isCurrentWeek: true},
      {id: '2018-09-30', isDisplayable: true, isCurrentWeek: true},
      {id: '2018-10-01', isDisplayable: true, isCurrentWeek: false},
      {id: '2018-10-02', isDisplayable: false, isCurrentWeek: false},
      {id: '2018-10-03', isDisplayable: false, isCurrentWeek: false},
      {id: '2018-10-04', isDisplayable: false, isCurrentWeek: false},
      {id: '2018-10-05', isDisplayable: false, isCurrentWeek: false},
      {id: '2018-10-06', isDisplayable: false, isCurrentWeek: false},
      {id: '2018-10-07', isDisplayable: false, isCurrentWeek: false}
    ];

    days.forEach(day => {
      batch.set(admin.firestore().collection('days').doc(day.id), {
        isDisplayable: day.isDisplayable, isCurrentWeek: day.isCurrentWeek
      });
    });

    batch.commit().then(() => {
      response.status(200).send('OK');
    }).catch(err => {
      response.status(400).send(err);
    });
  });
});

